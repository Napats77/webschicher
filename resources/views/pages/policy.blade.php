                            </p>
                        </div>

                        <div class="row" style="display: contents;">
                            <strong class="font-size-h4">ข้อ 7: การคุ้มครองข้อมูลการเงิน</strong>
                            <p>
                                การรักษาความลับข้อมูลด้านการเงินส่วนบุคคล เป็นเรื่องที่สําคัญอย่างยิ่งต่อบริษัท บริษัทจะไม่เผยแพร่ข้อมูลดังกล่าวต่อบุคคลภายนอก เว้นแต่เท่าที่ระบุไว้ในนโยบายส่วนบุคคลฉบับนี้ ดังนั้น ข้อมูลสถิติของผู้ใช้บริการ บริษัทอาจเปิดเผยข้อมูลที่ท่านให้ความยินยอมต่อผู้โฆษณา หรือพันธมิตรทางธุรกิจเกี่ยวกับบริการทางการเงิน รวมถึงการใช้บริการของลูกค้าของบริษัท
                            </p>
                        </div>


                        <div class="row" style="display: contents;">
                            <strong class="font-size-h4">ข้อ 8: ระยะเวลาในการเก็บข้อมูลส่วนบุคคลของท่าน</strong>
                            <p>
                                บริษัทตระหนักถึงความสำคัญของการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลของท่าน บริษัทจึงกำหนดให้มีมาตรการในการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลอย่างเหมาะสม และสอดคล้องกับการรักษาความลับของข้อมูลส่วนบุคคลเพื่อป้องกันการสูญหาย การเข้าถึง ทำลาย ใช้แปลง แก้ไข หรือ เปิดเผยข้อมูลส่วนบุคคลโดยไม่มีสิทธิหรือโดยไม่ชอบด้วยกฎหมาย ตลอดจนการป้องกันมิให้มีการนำข้อมูลส่วนบุคคลไปใช้โดยมิได้รับอนุญาต และดำเนินมาตรการที่เหมาะสมเพื่อคุ้มครองสิทธิของท่าน เพื่อให้เป็นไปตามกฎหมายคุ้มครองข้อมูลส่วนบุคคล โดยข้อมูลส่วนบุคคลของท่านที่บริษัทได้รับมาจะถูกนำไปใช้ให้เป็นไปตามวัตถุประสงค์การดำเนินงานของบริษัทเท่านั้น
                            </p>
